x = [3,2,4,2,1,5,8,5,7,6,9,7,5,2]

c0 = 0
c1 = 0
c2 = 0
c3 = 0
c4 = 0

for a in x:  #處理x中的每一個數字
    #計算a出現的次數
    if a==0:
        c0 = c0+1
    if a==1:
        c1 = c1+1
    if a==2:
        c2 = c2+1
    if a==3:
        c3 = c3+1
    if a==4:
        c4 = c4+1

print('0出現',c0,'次')
print('1出現',c1,'次')
print('2出現',c2,'次')